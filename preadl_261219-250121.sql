SELECT
	KEY
	,TO_CHAR(DSTAMP,'YYYY-MM-DD') AS TRANS_DATE
	,CLIENT_ID
	,SITE_ID
	,CODE
	,REFERENCE_ID
	,LINE_ID
	,SKU_ID
	,UPDATE_QTY AS QTY
	,TAG_ID
FROM INVENTORY_TRANSACTION_ARCHIVE
WHERE SITE_ID = 'SITE_ID'
AND CLIENT_ID = 'CLIENT_ID'
AND TO_CHAR(DSTAMP,'YYYY-MM-DD') >= '2019-12-26'
AND TO_CHAR(DSTAMP,'YYYY-MM-DD') < '2021-01-26'
AND CODE = 'Receipt'
ORDER BY TRANS_DATE, REFERENCE_ID, LINE_ID